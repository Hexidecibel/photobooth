/**
 * @constructor
 * @param {string} lang - Language code (e.g. 'en', 'fr', 'de')
 */
function I18n(lang) {
    this.lang = lang || 'en';
    this.strings = TRANSLATIONS[this.lang] || TRANSLATIONS.en;
}

/**
 * Get a translated string by key, falling back to English then the raw key.
 * @param {string} key
 * @returns {string}
 */
I18n.prototype.t = function (key) {
    return this.strings[key] || TRANSLATIONS.en[key] || key;
};

/**
 * Apply translations to all elements with data-i18n and data-i18n-placeholder
 * attributes in the DOM.
 */
I18n.prototype.apply = function () {
    var self = this;
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
        var key = el.getAttribute('data-i18n');
        el.textContent = self.t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
        var key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = self.t(key);
    });
};
